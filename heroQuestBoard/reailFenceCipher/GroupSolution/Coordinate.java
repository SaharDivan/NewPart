package heroQuestBoard.reailFenceCipher.GroupSolution;

public class Coordinate {

    private int column;
    private int row;

    public Coordinate(int x, int y) {
        this.column = x;
        this.row = y;
    }

    public int getColumn() {
        return column;
    }

    public int getRow() {
        return row;
    }
}
